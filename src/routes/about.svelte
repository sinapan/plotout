<h1>Plotout</h1>

<p>A web app that makes data visualization extremely fast and easy.</p>

<p>
  Plotout aims to make data exploration a breeze through fast and responsive
  visualizations. This tool enables data scientists and engineers to focus on
  making the best representation for their data instead of spending time
  figuring out how to code a decent data visualization. Plotout is also ideal
  for nontechnical personal who need to analyze data and optimize business
  logic.
</p>
